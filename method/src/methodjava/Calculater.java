package methodjava;

public class Calculater {
	int num1,num2;
	double addition(int num1,int num2)
	{
		int sum=num1+num2;
		return sum;
		
	}
	
	double subtraction(int num1,int num2)
	{
		int sub=num1-num2;
		return sub;
		
	}

	double multification(int num1,int num2)
	{
		int mul=num1*num2;
		return mul;
		
	}

	
	double divion(int num1,int num2)
	{
		int div=num1/num2;
		return div;
		
	}


}
